    private void checkAnswer(int index) {
        javax.swing.ButtonModel selectedModel = ChoiceButtonGroup.getSelection();        
        if(selectedModel != null) {
            //Answer's correct
            if (selectedModel.getActionCommand().equalsIgnoreCase(getCorrectAnswer().get(index))) {
                playSound(0, 500);
                if(helperQuantity > 0) {
                    fiftyFiftyButton.setEnabled(true);
                }
                setEnableRadioButton(true);
                setPointLabel(Integer.toString(++score));
                ChoiceButtonGroup.clearSelection();
                setSymbolQuestionLabel(questionNumber, true);
                questionNumber++;
                if (questionNumber < getQuestion().size()) {
                    setQuestionDetail(questionNumber);
                    return;
                } 
                gameWin();
            }
            //Answer's wrong
            else {
                playSound(1, 500);
                gameLose = true;
                setSymbolQuestionLabel(questionNumber, false);
                setEnableRadioButton(false);
                swapButton();
            }
        } 
        else {
            callOptionPane("Please choose your answer", "Answer Required", 2);
        }
    }
    
    private void gameWin() {
        gameLose = true;
        fiftyFiftyButton.setEnabled(false);
        setEnableRadioButton(false);
        playSound(2, 500);
        callOptionPane("Congratulations, you won this game", "Congratulations", 1);
        swapButton();
    }
    
    private void swapButton() {
        if(gameLose) {
            AnswerButton.setBackground(java.awt.Color.ORANGE);
            AnswerButton.setText("Retry");
            return;
        }
        AnswerButton.setBackground(java.awt.Color.GREEN);
        AnswerButton.setText("Answer");
    }
    
    private void fiftyFiftyHelper(int index) {        
        fiftyFiftyButton.setText("x" + (--helperQuantity));
        fiftyFiftyButton.setEnabled(false);
        ChoiceButtonGroup.clearSelection();
        
        for (int i = 0; i < getChoices().get(index).size(); i++) {

            boolean answer = getRadioButtonComponent()[i].getText().equalsIgnoreCase(getCorrectAnswer().get(index));
            boolean fiftyFiftyChoice = getRadioButtonComponent()[i].getText().equalsIgnoreCase(getFiftyFiftyChoice().get(index));
            
            if (answer || fiftyFiftyChoice ) {
                getRadioButtonComponent()[i].setEnabled(true);
            }
            else {
                getRadioButtonComponent()[i].setEnabled(false);
            }
        }
    }
    
    private void resetComponent() {            
        if(gameLose) {
            for(int index = 0; index < getQuestion().size(); index++) {
                getNumberQuestionLabel()[index].setForeground(java.awt.Color.YELLOW);
                getNumberQuestionLabel()[index].setText(Integer.toString(index + 1));
            }
            score = 0;
            questionNumber = 0;
            helperQuantity = 3;
            gameLose = false;
            fiftyFiftyButton.setText("x" + helperQuantity);
            fiftyFiftyButton.setEnabled(true);
            ChoiceButtonGroup.clearSelection();
            setPointLabel("0"); 
            setEnableRadioButton(true);
            swapButton();
            setQuestionDetail(questionNumber);
        }
        return;
    }
    
    private void playSound(int selectSound, int sleepTime) {
        soundController.play(selectSound);
        try {
            Thread.sleep(sleepTime);
        } 
        catch (Exception e) {
            callOptionPane("Error", e.toString(), 0);
        }
    }
    
