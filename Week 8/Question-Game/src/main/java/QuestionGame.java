    private List<QuestionJson> getQuestionJson() {
        try (InputStream inputStream = getClass().getResourceAsStream("/data/Question.json")) {
            InputStreamReader reader = new InputStreamReader(inputStream, StandardCharsets.UTF_8);
            return new Gson().fromJson(reader, new TypeToken<List<QuestionJson>>() {}.getType());
        } 
        catch (Exception e) {
            callOptionPane(e.toString(), "Error", 0);
            this.dispose();
            return null;
         } 
    }
    
    public List<String> getQuestion() {
        List<String> questionList = new ArrayList<>();
        
        for (QuestionJson q : questionData) {
            questionList.add(q.question);
        }
        return questionList;
    }
    
    public List<String> getCorrectAnswer() {
        List<String> answerList = new ArrayList<>();
        
        for (QuestionJson q : questionData) {
            answerList.add(q.answer);
        }
        return answerList;
    }
    
