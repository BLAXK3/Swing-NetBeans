    private void setQuestionDetail(int index) {
        QuestionLabel.setText("<html>" + getQuestion().get(index) + "</html>");
        List<String> choiceNumber = getChoices().get(index);
        java.util.Collections.shuffle(choiceNumber);
        
        for (int indexs = 0; indexs < choiceNumber.size(); indexs++) {
            getRadioButtonComponent()[indexs].setText(choiceNumber.get(indexs));
            getRadioButtonComponent()[indexs].setActionCommand(choiceNumber.get(indexs));
        }
    }
    
